// Example of a for loop:

for (var counter = 1; counter < 11; counter++) {
	console.log(counter);
}

// Change where the for loop starts.

for (var i = 5; i < 11; i = i + 1){
	console.log(i);
}

// Edit this for loop

for (var i = 4; i < 24; i = i + 1) {
	console.log(i);
}

// Increment by 5

for (var i = 5; i < 51; i+=5) {
	console.log(i);
}

// Begin at 8 and increment by 12

for (var i = 8 ; i < 121; i+=12) {
	console.log(i);
}

// Count down from 10 to 0

for (var i = 10; i >= 0; i--) {
	console.log(i);
}

// Start at 100, decrement by five, end at five

for (i = 100; i >= 1; i-=5) {
    console.log(i);   
}

